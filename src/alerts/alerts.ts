import { toast } from "react-toastify";

export const alertsuccess = (msj: string) => {
  toast.success(msj, {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

